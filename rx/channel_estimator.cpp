// Channel Estimator - Pilot-based — II=1 FIX
//
// WARNING W8 fixed: collect_bins loop now achieves II=1 (was II=2).
//
// Root cause of II=2:
//   pilot_cnt is READ (comparison against pilot_positions[]) and WRITTEN
//   (pilot_cnt++) in the same pipeline iteration.
//   Carried dependence distance=1 -> tool cannot achieve II=1.
//
//   Additionally data_wr_cnt has the same problem:
//   READ (< N_DATA guard) and WRITTEN (data_wr_cnt++) in the same iteration.
//
// Fix: replace BOTH loop-carried counters with precomputed static ROMs:
//
//   pilot_flag[N_USED]      — 1-bit ROM: is this active subcarrier a pilot?
//   data_index_rom[N_USED]  — 10-bit ROM: which data buffer slot to write?
//
// Both ROMs are READ-ONLY inside collect_bins.
// The only remaining loop-carried operations are the pilot accumulator
// additions (pilot_acc_i += ...) which are add-only recurrences that HLS
// handles at II=1 with a single adder.
// data_buf_i/q[data_index_rom[offset]] is a direct addressed write with
// no read-before-write in the same iteration -> no dependence.
//
// Result: collect_bins achieves II=1.
//
// Note on data_index_rom values at pilot positions:
//   The value is 0 (harmless) — it is never accessed because pilot_flag
//   guards the write. pilot_flag and data_index_rom are both derived from
//   the same pilot_positions[] so they are always consistent.
//============================================================================
